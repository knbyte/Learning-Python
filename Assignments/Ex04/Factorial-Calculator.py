#	To complete the "Factorial Calculator" assignment, you'll need to understand            # 
#	recursion or iteration (depending on your preferred approach) and how to                #
#	implement factorial calculations.                                                       #
#                                                                                           #
#	- Understanding of Factorial:                                                           #
#		Factorial of a non-negative integer n is the product of all positive                #
#		integers less than or equal to n.                                                   #
#		For example, factorial of 5 (written as 5!) is 5 × 4 × 3 × 2 × 1 = 120.                                            #
#                                                                                           #
# 	- Recursion or Iteration:                                                               #
#		You can calculate factorial using either recursion or iteration. Recursion          #
#		involves a function calling itself, while iteration involves looping through        #
#		a range of numbers. You'll need to understand one of these approaches.              #
#                                                                                           #
#	- Control Structures:                                                                   #
#		If you choose the iterative approach, you'll use loops (such as for loops) to       #
#		iterate through the numbers and calculate the factorial. If you choose the          #
#		recursive approach, you'll use control structures to handle the recursion.          #
#                                                                                           #
#                                                                                           #
#	- Basic Outline:                                                                        #
#		You'll Need to:                                                                     #
#		Prompt the user to enter a non-negative integer for which they want to              #
#			to calculate the factorial.                                                     #
#		Implement a function to calculate the factorial using either recursion or iteration.#
#		Display the result to the user.                                                     #
